    prefix="${ts:0:1}"
    name="${ts:1}"
    case "$prefix" in
      #"+") outpath="data/train/${name}.txt"; errpath="error/train/${name}.err" ;;
      #"=") outpath="data/dev/${name}.txt"  ; errpath="error/dev/${name}.err" ;;
      "?") outpath="data/test/${name}.txt" ; errpath="error/test/${name}.err" ;;
      *)
        echo "Testsuite not designated as train (+), dev (=) or test (?): $ts"
        exit 1
        ;;
    esac
    echo "Converting $base/$name" >&2
    python3 mrs_to_penman_old.py \
        -i "$base/$name" \
        -p parameters.json \
        > "$outpath" 2> "$errpath"
done
